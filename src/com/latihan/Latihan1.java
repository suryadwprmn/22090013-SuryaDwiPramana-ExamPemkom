   public void tampilData(){
         DefaultTableModel tbl = new DefaultTableModel();
    tbl.addColumn("No");
    tbl.addColumn("Nama");
    tbl.addColumn("Gender");
    tbl.addColumn("Tempat");
    tbl.addColumn("Tgl_Lahir");
    tbl.addColumn("Alamat");
    tbl.addColumn("No_HP");
    
    try {
        Connection conn = connect.koneksiDB();
        String sql = "SELECT * FROM tbdata";
        PreparedStatement pst = conn.prepareStatement(sql);
        ResultSet rs = pst.executeQuery();

        while (rs.next()) {
            tbl.addRow(new Object[]{
                rs.getString(1),
                rs.getString(2),
                rs.getString(3),
                rs.getString(4),
                rs.getString(5),
                rs.getString(6),
                rs.getString(7),
            });
        }

        TableMhs.setModel(tbl);

    } catch (Exception e) {
        e.printStackTrace();
    }
    }
   
