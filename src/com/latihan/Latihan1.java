    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
         try {
        String sql = "UPDATE tbdata SET "
                + "nama='" + nama.getText() + "', "
                + "gender='" + gender.getSelectedItem() + "', "
                + "tempat_lahir='" + tempatLahir.getText() + "', "
                + "tgl_lahir='" + tglLahir + "', "
                + "alamat='" + alamat.getText() + "', "
                + "no_hp='" + noHp.getText() + "' "
                + "WHERE no='" + no.getText() + "'";
        
        Connection conn = connect.koneksiDB();
        java.sql.PreparedStatement pst = conn.prepareStatement(sql);
        int affectedRows = pst.executeUpdate();
        
        if (affectedRows > 0) {
            JOptionPane.showMessageDialog(null, "Berhasil diedit");
            tampilData();
        } else {
              JOptionPane.showMessageDialog(null, "Gagal diedit. Tidak ada data yang terpengaruh.");
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Gagal diedit. Error: " + e.getMessage());
        e.printStackTrace(); // Menampilkan stack trace untuk debugging
    }
        
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
        // TODO add your handling code here:
        try {
     Connection conn = connect.koneksiDB();
    String sql = "Select * from tbdata where no='"+cari.getText()+"' or nama='"+cari.getText()+"'";
    java.sql.PreparedStatement pst = conn.prepareStatement(sql);
    ResultSet rs = pst.executeQuery(sql);
    if (rs.next()){
        no.setText(rs.getString(1));
        nama.setText(rs.getString(2));
        gender.setSelectedItem(rs.getString(3));
        tempatLahir.setText(rs.getString(4));
        tgl.setDate(rs.getDate(5));
        alamat.setText(rs.getString(6));
        noHp.setText(rs.getString(7));
        JOptionPane.showMessageDialog(null,"Data tbdata : "
        + cari.getText() + " ditemukan, Apakah Anda Ingin Mengedit atau Menghapus?");
    }
} catch (Exception e){
}
cari.requestFocus();
    }//GEN-LAST:event_btnCariActionPerformed

