    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
          try {
        String sql = "INSERT INTO tbdata VALUES (?, ?, ?, ?, ?, ?, ?)";
        Connection conn = connect.koneksiDB();
        PreparedStatement pst = conn.prepareStatement(sql);

        pst.setString(1, no.getText());
        pst.setString(2, nama.getText());
        pst.setString(3, gender.getSelectedItem().toString());
        pst.setString(4, tempatLahir.getText());
        pst.setString(5, tglLahir);  // Use the formatted date
        pst.setString(6, alamat.getText());
        pst.setString(7, noHp.getText());

        pst.execute();
        JOptionPane.showMessageDialog(null, "Save Success");
        tampilData();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Gagal tersimpan");
        System.out.println(e.getMessage());
        e.printStackTrace();
    }
    }//GEN-LAST:event_btnSimpanActionPerformed

