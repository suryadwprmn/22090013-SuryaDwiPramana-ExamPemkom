    private void updateNamaKasir() {
    String nik = txtNik.getText();
    if (!nik.isEmpty()) {
        try {
            Connection conn = (Connection) Koneksi.koneksiDB();
            String sql = "SELECT nama_kasir FROM kasir WHERE nik='" + nik + "'";
             PreparedStatement preparedStatement = conn.prepareStatement(sql);
                ResultSet rs = preparedStatement.executeQuery();

                if (rs.next()) {
                    String namaKasir = rs.getString("nama_kasir");
                    txtNamaKasir.setText(namaKasir);
                } else {
                    txtNamaKasir.setText(""); // NIK tidak ditemukan, kosongkan nama kasir
                }
            
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    } 
}
 
