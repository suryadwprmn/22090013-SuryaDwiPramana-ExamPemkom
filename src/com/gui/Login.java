    private void btnLoginMouseClickActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginMouseClickActionPerformed
        // TODO add your handling code here:
          String nik = String.valueOf(txtNik.getText());
          String password = String.valueOf(txtPassword.getPassword());
            if (nik.isEmpty() ) {
            JOptionPane.showMessageDialog(null,"NIK tidak boleh kosong");
        } else if(password.isEmpty()){
            JOptionPane.showMessageDialog(null, "Password tidak boleh kosong");
        }else{
            
          try {
        Connection conn = (Connection) Koneksi.koneksiDB();
        Statement st = conn.createStatement();
        String sql= "SELECT * FROM kasir WHERE nik='"+ nik+"' AND password='"+password+"'";  
            ResultSet rs = st.executeQuery(sql);
            
            if(rs.next()){
               String namaKasir = rs.getString("nama_kasir");
              JOptionPane.showMessageDialog(null, "Sign In Successful", "Sign In", JOptionPane.INFORMATION_MESSAGE);
              JOptionPane.showMessageDialog(null, "Login sebagai " + namaKasir , "Sign In", JOptionPane.INFORMATION_MESSAGE);
                    mainApp.setNamaKasir(namaKasir);
                    mainApp.setVisible(true);
                    this.dispose();
            } else{
            JOptionPane.showMessageDialog(null, "Password yang anda masukkan salah!","Error",JOptionPane.ERROR_MESSAGE);
            }
          } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
        
    }//GEN-LAST:event_btnLoginMouseClickActionPerformed

